    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $credentials=$request->validate([
            'email' => 'required|email',
            'password' => 'required|min:6'

        ]);
        try{
            //dd(Hash::make('123456'));
            $email = $credentials['email'];
            $password = $credentials['password'];
            $remember = $request->has('rememberme') ? true : false; 
            //dd($credentials);
            if (Auth::attempt(['email' => $email, 'password' => $password, 'is_active'=> 1], $remember)) {
               
                return redirect()->route('admin.dashboard');
            }
            
            return Redirect::back()->withInput(['error' => 'Invalid login credentials!!!']);
        }
    
        catch(\Exception $e) {
            return Redirect::back()->withInput(['error' => $e->getMessage()]);
        }
    }
    

